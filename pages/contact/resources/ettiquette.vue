                <h1>Class Flow</h1>
                <p>This describes the formal procedures for normal classtime.</p>
                <section>
                    <h3>1. Entering the Dojo</h3>
                    <p>Bow before crossing the threshold of the dojo. Place your gear on the ground, grab your gi, and get changed. Leave water with your gear.</p>
                    <p>Shoes should be taken off before entering the main area, if an area is provided; otherwise, take them off immediately after entering.</p>
                </section>
                <section>
                    <h3>2. Entering the Floor</h3>
                    <p>Once in full gi, bow before entering the floor. Use this time to warm up or practice independently.</p>
                    <p>If an instructor approaches you, bow and greet them (see Languages).</p>
                    <p>If you arrive late for class, kneel at the edge of the room until acknowledged by the senseis.</p>
                </section>
                <section>
                    <h3>3. Lining up for Class</h3>
                    <p>One of the higher students will yell "Seidetsu."</p>
                    <p>Students will line up according to rank. White belts will be the last line. Kyu ranks will line up according to rank, with the lowest in the bottom left and the highest in the top right. All lines will be alligned with the rightmost
                        student in the line. Next are the 1st and 2nd Dan Blackbelts, followed progressively by lines of higher blackbelts.</p>
                </section>
                <section>
                    <h3>4. Bowing In</h3>
                    <p>The student at the furthest forward right will call "Kiotsuke." When they next call "Ushiro ni muite gi to obi o noasu," step behind and turn to your right to face the back. Use this opportutnity to straighten out your gi. Never straighten
                        out your gi facing a highranking student. When they call "Shomen," turn right again to face the front. When they call "Sensei ni, Rey," face the highest ranking individual in the front of the room and bow while greeting them appropriately.
                        Continue progressively for as many times as the leader calls.</p>
                </section>
                <section>
                    <h3>5. Breaks</h3>
                </section>
                <section>
                    <h3>6. Weapons</h3>
                    <p>When handling weapons, keep them close to your sides to avoid injury.</p>
                    <p>Never handle another person's weapons without permission. When receiving weapons, bow and receive them handle-first, palm-up. When presenting weapons, present them handle first and bow.</p>
                </section>
                <section>
                    <h3>7. Receiving Help</h3>
                    <p>To flag down an instructor for help, raise your hand palm back and wait for the to come to you.</p>
                    <p>When they are done teaching, say "Domo arigato gozaimasu," cutting off either "Domo" and/or "gozaimasu" depending on how much higher-ranked they are than you (higher rank = longer thanks).</p>
                </section>
                <section>
                    <h3>8. Bowing Out</h3>
                    <p>The student at the furthest forward right will call "Kiotsuke." When they next call "Ushiro ni muite gi to obi o noasu," step behind and turn to your right to face the back. Use this opportutnity to straighten out your gi. Never straighten
                        out your gi facing a highranking student. When they call "Shomen," turn right again to face the front. When they call "Sensei ni, Rey," face the highest ranking individual in the front of the room and bow while thanking them. Continue
                        progressively for as many times as the leader calls. It may be the case that each student will bow to the next lowest student. In this case, follow suit an turn to the right to face the appropriate direction. In all cases, bow
                        towards the heads and give them the appropriate farewell. Let them exit the dojo before leaving.</p>
